#include "libft.h"

int	ft_isalnum(int x)
{
	if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z') || \
	(x >= 48 && x <= 57))
	{
		return (1);
	}
	return (0);
}
